package FC;

public class FeatureFUF {
    private int feature;
    private double fuf;

    public FeatureFUF(int feature, double fuf){
        this.feature = feature;
        this.fuf = fuf;
    }

    public int getFeature() {
        return feature;
    }

    public double getFuf() {
        return fuf;
    }
}
